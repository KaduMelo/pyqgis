x = 3
y = 3

# Python Arithmetic Operators
# +	Addition	
print(x + y)

# -	Subtraction	
print(x - y)

# *	Multiplication
print(x * y)

# /	Division
print(x / y)

# %	Modulus	
print(x % y)
print(5 % 2)

# **	Exponentiation
x = 2
y = 5

print(x ** y) #same as 2*2*2*2*2

# //	Floor division
x = 24
y = 8
print(x // y)

# Python Comparison Operators
# Equal
print(x == y)

# Not Equal
print(x != y)

# Greater than
print(x > y)

# Less than
print(x < y)

# Greater than or equal to
print(x >= y)

# Less than or equal to
print(x <= y)

# Python Logical Operators
# and 
print(x < 5 and  x < 10)

# and 
print(not(x < 5 and x < 10))

# Python Identity Operators
# is
print(x is y)

# is not
print(x is not y)